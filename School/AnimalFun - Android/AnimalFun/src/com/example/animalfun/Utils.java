package com.example.animalfun;

import android.app.Activity;


public class Utils {

	public static int theme = 0;
	public static boolean changed = false;

	
	
	public static void setThemeToActivity( Activity act, int myTheme )
	{
		
		try {
			switch(theme) {
	        case 0:
	        	act.setTheme(R.style.LightTheme);
	        	break;
	        case 1:
	        	act.setTheme(R.style.DarkTheme);
	        	break;
	        }
			
			
		}
		catch (Exception e) {}
		
	}
}
